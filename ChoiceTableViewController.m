#pragma mark Table view methods

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}


// Customize the number of rows in the table view.
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [choiceList count];
}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
	}
    
	NSString* choice = [choiceList objectAtIndex:[indexPath row]];
	if([choice isEqualToString:selectedChoice]){
	
		[cell setAccessoryType:UITableViewCellAccessoryCheckmark];
		selectedChoiceIndex = [indexPath row];
	}else{
        [cell setAccessoryType:UITableViewCellAccessoryNone];
    }
	
	[[cell textLabel] setText:choice];
	
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {

	UITableViewCell* cell = nil;
	if(selectedChoiceIndex!=-1){
		
		NSIndexPath* index= [NSIndexPath indexPathForRow:selectedChoiceIndex inSection:0];
		cell = [tableView cellForRowAtIndexPath:index];
		[cell setAccessoryType:UITableViewCellAccessoryNone];
	}
	
	selectedChoiceIndex = [indexPath row];
	cell = [tableView cellForRowAtIndexPath:indexPath];
	[cell setAccessoryType:UITableViewCellAccessoryCheckmark];
	
	selectedChoice = [[cell textLabel] text];
	[delegate performSelector:@selector(setValue:) withObject:selectedChoice];
    
    [self.navigationController popViewControllerAnimated:YES];
}

